def fetch_underlying_intraday(
    kite: KiteConnect,
    underlying_token: int,
    interval: str,
    day: Optional[pd.Timestamp] = None,
) -> pd.DataFrame:
    now = _now_local()
    day = day or now
    start = day.normalize() + pd.Timedelta(hours=9, minutes=0)
    end = now  # current time
    candles = kite.historical_data(
        instrument_token=underlying_token,
        from_date=start.to_pydatetime(),
        to_date=end.to_pydatetime(),
        interval=interval,
        continuous=False,
        oi=False,
    )
    df = pd.DataFrame(candles)
    if not df.empty and "date" in df.columns:
        df["date"] = pd.to_datetime(df["date"], errors="coerce")
        # normalize expected names for test_signals pipeline
        df = df.rename(columns={"date": "datetime"})
    return df


