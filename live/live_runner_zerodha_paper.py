def _now_local() -> pd.Timestamp:
    # Use local machine time; your signals/candles are IST
    return pd.Timestamp.now(tz="Asia/Kolkata")


def _parse_hhmm(hhmm: str) -> Tuple[int, int]:
    h, m = hhmm.split(":")
    return int(h), int(m)


def _today_time(hhmm: str, ref: Optional[pd.Timestamp] = None) -> pd.Timestamp:
    ref = ref or _now_local()
    h, m = _parse_hhmm(hhmm)
    return ref.normalize() + pd.Timedelta(hours=h, minutes=m)


def in_time_window(now: pd.Timestamp, start_hhmm: str, end_hhmm: str) -> bool:
    start = _today_time(start_hhmm, now)
    end = _today_time(end_hhmm, now)
    return start <= now <= end


