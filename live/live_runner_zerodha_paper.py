# ============================================================
def run_strategy_and_capture(df_raw: pd.DataFrame) -> Tuple[pd.DataFrame, List[Any]]:
    """
    Calls your test_signals.main() but monkey-patches prepare_df and generate_signals
    so we can inject our live df and capture prepared+signals.

    Assumes:
      - test_signals.py exports prepare_df, generate_signals, main
      - prepare_df(df) and generate_signals(df, ...)
      - main() internally reads data & calls these functions
    """
    import core.test_signals as mod

    captured: Dict[str, Any] = {
        "prepared": None,
        "signals": None,
    }

    orig_prepare_df = mod.prepare_df
    orig_generate_signals = mod.generate_signals

    # Save original pd.read_csv if main() reads CSV internally
    orig_read_csv = None
    if hasattr(mod, "pd") and hasattr(mod.pd, "read_csv"):
        orig_read_csv = mod.pd.read_csv

    # Wrapper: ignore whatever main loaded, use our live df_raw
    def wrapped_prepare_df(_df_ignored, *args, **kwargs):
        out = orig_prepare_df(df_raw.copy(), *args, **kwargs)
        captured["prepared"] = out
        return out

    # Wrapper: inject our generation params and capture signals
    def wrapped_generate_signals(df, *args, **kwargs):
        # Force runtime profile config even if test_signals.main() passes hardcoded params.
        kwargs = dict(kwargs)
        kwargs["target_points"] = TARGET1_POINTS
        kwargs["touch_tolerance"] = TOUCH_TOLERANCE
        kwargs["break_confirmation"] = BREAK_CONFIRMATION
        kwargs["sl_buffer_points"] = SL_BUFFER_POINTS
        kwargs["min_body_points"] = MIN_BODY_POINTS
        kwargs["allow_long_pp"] = ALLOW_LONG_PP_SIGNAL
        kwargs["allow_short_pp"] = ALLOW_SHORT_PP_SIGNAL
        kwargs["entry_cutoff_hhmm"] = SIGNAL_ENTRY_CUTOFF_HHMM
        kwargs["pp_entry_cutoff_hhmm"] = PP_ENTRY_CUTOFF_HHMM
        kwargs["max_candle_range_points"] = MAX_CANDLE_RANGE_POINTS
        kwargs["max_sl_distance_points"] = MAX_SL_DISTANCE_POINTS

        out = orig_generate_signals(df, *args, **kwargs)
        captured["signals"] = out
        return out

    # Optional patch: if main() reads CSV, return our df
    def wrapped_read_csv(*args, **kwargs):
        return df_raw.copy()

    try:
        mod.prepare_df = wrapped_prepare_df
        mod.generate_signals = wrapped_generate_signals
        if orig_read_csv is not None:
            mod.pd.read_csv = wrapped_read_csv

        # main() may print a lot; that's okay for debugging
        mod.main()

    finally:
        mod.prepare_df = orig_prepare_df
        mod.generate_signals = orig_generate_signals
        if orig_read_csv is not None:
            mod.pd.read_csv = orig_read_csv

    prepared: pd.DataFrame = captured["prepared"] # type: ignore
    signals: List[Any] = captured["signals"] # type: ignore

    if prepared is None:
        raise RuntimeError("Strategy run failed: prepare_df output was not captured.")
    if not isinstance(signals, list):
        raise RuntimeError("Strategy run failed: generate_signals output was not captured as list.")

    return prepared, signals


