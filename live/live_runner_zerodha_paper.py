def main():
    args = _parse_runtime_args()
    profile = args.profile
    execution_mode = str(args.mode).upper()
    apply_strategy_profile(profile)
    if execution_mode == "LIVE" and str(args.confirm).strip() != LIVE_CONFIRM_TEXT:
        raise RuntimeError(f"Live mode blocked. Re-run with: --mode live --confirm {LIVE_CONFIRM_TEXT}")

    print("=" * 80)
    print(f"LIVE RUNNER (ZERODHA) - MODE={execution_mode}")
    print("=" * 80)
    print(f"Profile: {profile}")
    print(
        f"Underlying source: mode={UNDERLYING_SIGNAL_MODE} "
        f"spot={UNDERLYING_SYMBOL} fut_name={UNDERLYING_FUT_NAME} ({UNDERLYING_EXCHANGE}) "
        f"| Options on {OPTION_UNDERLYING_NAME}"
    )
    print(f"Interval: {CANDLE_INTERVAL} | Poll every: {POLL_EVERY_SECONDS}s")
    print(f"Entry window: {ALLOW_ENTRY_FROM} to {ALLOW_ENTRY_TILL} | Force exit: {FORCE_EXIT_TIME}")
    print(f"MAX_TRADES_PER_DAY={MAX_TRADES_PER_DAY} | STOP_FILE={STOP_FILE}")
    print(f"MAX_DAILY_LOSS_RUPEES={MAX_DAILY_LOSS_RUPEES:.2f} | LIVE_PRODUCT={LIVE_PRODUCT}")
    if execution_mode == "LIVE":
        print("WARNING: LIVE mode places REAL ORDERS.")
    print()

    try:
        kite = make_kite()
    except Exception as e:
        log_error("auth", e)
        sys.exit(1)

    try:
        nse_df, nfo_df = load_instruments(kite)
        underlying_token, underlying_label = get_underlying_token(
            nse_df=nse_df,
            nfo_df=nfo_df,
            symbol=UNDERLYING_SYMBOL,
            fut_name=UNDERLYING_FUT_NAME,
            signal_mode=UNDERLYING_SIGNAL_MODE,
        )
        print(f"Underlying token selected: {underlying_token} ({underlying_label})")
    except Exception as e:
        log_error("instrument_setup", e)
        sys.exit(1)

    state = EngineState()

    while True:
        state.loop_count += 1
        now = _now_local()

        # Kill switch
        if os.path.exists(STOP_FILE):
            print(f" STOP file detected ({STOP_FILE}). Exiting runner safely.")
            break

        # Reset state on day change
        day_str = str(now.date())
        if state.today_date != day_str:
            state.reset_for_new_day(day_str)

        # Heartbeat
        if state.loop_count % PRINT_HEARTBEAT_EVERY_LOOPS == 0:
            running_net = closed_net_pnl_rupees(state.closed_trades)
            print(
                f"[{now.strftime('%Y-%m-%d %H:%M:%S %Z')}] heartbeat | "
                f"open={len(state.open_trades)} closed={len(state.closed_trades)} "
                f"trades_today={state.trades_today_count} closed_net={running_net:.2f}"
            )

        # Daily loss kill-switch
        running_net = closed_net_pnl_rupees(state.closed_trades)
        if running_net <= -abs(MAX_DAILY_LOSS_RUPEES):
            print(
                f" Daily loss kill-switch triggered: {running_net:.2f} <= -{abs(MAX_DAILY_LOSS_RUPEES):.2f}. "
                f"No further entries for today."
            )
            if execution_mode == "LIVE" and state.open_trades:
                for tr in state.open_trades:
                    ok, msg = flatten_live_trade(kite, tr)
                    if not ok:
                        log_error("live_kill_switch_flatten", msg, {"trade_id": tr.get("trade_id"), "opt_symbol": tr.get("opt_symbol")})
                    else:
                        print(f" LIVE FLATTEN | {msg}")
            break

        # Outside market hours: just sleep
        if not in_time_window(now, MARKET_START, MARKET_END):
            time.sleep(POLL_EVERY_SECONDS)
            continue

        try:
            # 1) Fetch underlying intraday candles
            raw_df = fetch_underlying_intraday(kite, underlying_token, CANDLE_INTERVAL)
            if raw_df.empty:
                raise RuntimeError("No underlying candles returned.")

            # Ensure required columns exist
            expected_cols = {"datetime", "open", "high", "low", "close"}
            missing = expected_cols - set(raw_df.columns)
            if missing:
                raise RuntimeError(f"Underlying candles missing columns: {missing}. Columns={list(raw_df.columns)}")

            raw_df = raw_df.sort_values("datetime").reset_index(drop=True)

            # 2) Identify latest completed candle (avoid current in-progress candle)
            # For 5m, safest is second last row if latest row may be current.
            if len(raw_df) < 3:
                time.sleep(POLL_EVERY_SECONDS)
                continue

            latest_completed_candle_time = pd.to_datetime(raw_df.iloc[-2]["datetime"], errors="coerce")
            if pd.isna(latest_completed_candle_time):
                raise RuntimeError("Latest completed candle time invalid.")

            # If no new completed candle since last cycle, still update open trades then sleep
            new_candle_available = (
                state.last_completed_candle_time is None
                or latest_completed_candle_time > state.last_completed_candle_time
            )

            # 3) Update open trades every loop (option candles till now)
            if state.open_trades:
                still_open = []
                for tr in state.open_trades:
                    try:
                        opt_token = int(tr["opt_token"])
                        opt_df = fetch_option_intraday_from_signal_time(
                            kite, opt_token, pd.to_datetime(tr["entry_time_signal"], errors="coerce"), interval=CANDLE_INTERVAL
                        )
                        tr = _update_trade_from_option_candles(tr, opt_df, now)
                        if tr["status"] == "CLOSED":
                            if execution_mode == "LIVE" and not tr.get("live_exit_order_id"):
                                ok, msg = flatten_live_trade(kite, tr)
                                if not ok:
                                    tr["error"] = msg
                                    log_error("live_exit_open_trade", msg, {"trade_id": tr.get("trade_id"), "opt_symbol": tr.get("opt_symbol")})
                                else:
                                    print(f" LIVE EXIT | {tr.get('trade_id')} | {msg}")
                            state.closed_trades.append(tr)
                            append_csv_row(TRADES_LOG_CSV, trade_to_log_row(tr))
                            print(
                                f" {execution_mode} TRADE CLOSED | {tr['opt_symbol']} | "
                                f"reason={tr['close_reason']} | net={tr['net_pnl_rupees']:.2f}"
                            )
                        else:
                            still_open.append(tr)
                    except Exception as e:
                        tr["error"] = str(e)
                        log_error("update_open_trade", e, {"trade_id": tr.get("trade_id"), "opt_symbol": tr.get("opt_symbol")})
                        still_open.append(tr)
                state.open_trades = still_open

            if not new_candle_available:
                time.sleep(POLL_EVERY_SECONDS)
                continue

            # Mark processed candle
            state.last_completed_candle_time = latest_completed_candle_time
            print(f"\n New completed candle detected: {latest_completed_candle_time}")

            # 4) Run strategy pipeline on full intraday df (your test_signals logic)
            prepared_df, all_signals = run_strategy_and_capture(raw_df)

            # 5) Filter signals to the latest completed candle only (new ones)
            # We want signals whose entry_time == latest_completed_candle_time
            latest_signals = []
            for s in all_signals:
                st = sig_time(s)
                if pd.isna(st):
                    continue
                if st == latest_completed_candle_time:
                    latest_signals.append(s)

            if not latest_signals:
                print("No new signal on latest completed candle.")
                _debug_latest_candle_no_signal(prepared_df, latest_completed_candle_time)
                time.sleep(POLL_EVERY_SECONDS)
                continue

            print(f"Signals on latest candle: {len(latest_signals)}")

            # 6) Process signals with gating
            for s in latest_signals:
                st = sig_time(s)
                side = sig_side(s)
                fut_entry = sig_entry(s)
                fut_sl = sig_sl(s)
                level_tag = sig_level_tag(s)

                # Time window for entries
                if not in_time_window(now, ALLOW_ENTRY_FROM, ALLOW_ENTRY_TILL):
                    print(f" Signal ignored (outside entry window): {st} {side}")
                    continue

                # Max trades/day
                if state.trades_today_count >= MAX_TRADES_PER_DAY:
                    print(f" Max trades/day reached ({MAX_TRADES_PER_DAY}). Signal ignored.")
                    continue

                # Dedup by signal time
                st_key = str(st)
                if ENABLE_DEDUP_BY_SIGNAL_TIME and st_key in state.seen_signal_times_today:
                    print(f" Duplicate signal-time skipped: {st_key}")
                    continue

                # Dedup by trade key (time+side+level)
                trade_key = f"{st_key}|{side}|{level_tag}|{round(float(fut_entry), 2)}"
                if ENABLE_DEDUP_BY_TRADEKEY and trade_key in state.seen_trade_keys_today:
                    print(f" Duplicate trade-key skipped: {trade_key}")
                    continue

                # Optional: prevent opening multiple simultaneous trades (keep it simple)
                if len(state.open_trades) > 0:
                    print(" Existing open trade present. Skipping new signal (single-position mode).")
                    continue

                # Log signal
                append_csv_row(SIGNALS_LOG_CSV, signal_to_log_row(s))

                # Map option contract
                try:
                    contract_row, err = find_option_contract(
                        nfo_df=nfo_df,
                        signal_time=st,
                        underlying_name=OPTION_UNDERLYING_NAME,
                        side=side,
                        fut_entry_price=fut_entry,
                    )
                    if err or contract_row is None:
                        raise RuntimeError(err or "Option contract mapping failed")

                    option_token = int(contract_row["instrument_token"])
                    option_symbol = str(contract_row["tradingsymbol"])
                    opt_lot_size = int(contract_row.get("lot_size", 0) or 0)
                    if opt_lot_size <= 0:
                        raise RuntimeError(f"Invalid lot_size for {option_symbol}: {opt_lot_size}")
                    opt_qty = int(opt_lot_size * max(1, LOTS))

                    # Fetch option candles for opening
                    opt_df = fetch_option_intraday_from_signal_time(kite, option_token, st, interval=CANDLE_INTERVAL)
                    live_entry_price = None
                    live_entry_time = None
                    live_entry_order_id = None
                    live_entry_status = None

                    if execution_mode == "LIVE":
                        buy_oid = place_market_order(
                            kite=kite,
                            exchange=OPTION_EXCHANGE,
                            tradingsymbol=option_symbol,
                            tx_type=kite.TRANSACTION_TYPE_BUY,
                            qty=opt_qty,
                            product=LIVE_PRODUCT,
                        )
                        buy_snap = wait_for_order_status(kite, buy_oid, {"COMPLETE", "REJECTED", "CANCELLED"}, LIVE_ORDER_TIMEOUT_SEC)
                        buy_status = str((buy_snap or {}).get("status", "UNKNOWN")).upper()
                        if buy_status != "COMPLETE":
                            raise RuntimeError(
                                f"Live entry order not complete: symbol={option_symbol} order_id={buy_oid} status={buy_status}"
                            )
                        quote = kite.quote([f"{OPTION_EXCHANGE}:{option_symbol}"])
                        ltp_fallback = float(quote[f"{OPTION_EXCHANGE}:{option_symbol}"]["last_price"])
                        live_entry_price = _order_avg_fill_price(buy_snap, fallback=ltp_fallback)
                        live_entry_time = _now_local()
                        live_entry_order_id = str(buy_oid)
                        live_entry_status = buy_status

                    trade = open_paper_trade_from_signal(
                        s,
                        contract_row,
                        opt_df,
                        execution_mode=execution_mode,
                        forced_entry_price=live_entry_price,
                        forced_entry_time=live_entry_time,
                    )
                    trade["opt_qty"] = opt_qty
                    trade["live_entry_order_id"] = live_entry_order_id
                    trade["live_entry_status"] = live_entry_status

                    # Assign IDs / mark seen
                    state.trades_today_count += 1
                    trade_id = f"{day_str.replace('-', '')}-{state.trades_today_count:03d}"
                    trade["trade_id"] = trade_id

                    state.seen_signal_times_today.add(st_key)
                    state.seen_trade_keys_today.add(trade_key)

                    # Immediately update once (same loop)
                    trade = _update_trade_from_option_candles(trade, opt_df, now)

                    if trade["status"] == "CLOSED":
                        if execution_mode == "LIVE" and not trade.get("live_exit_order_id"):
                            ok, msg = flatten_live_trade(kite, trade)
                            if not ok:
                                trade["error"] = msg
                                log_error("live_exit_same_loop", msg, {"trade_id": trade.get("trade_id"), "opt_symbol": option_symbol})
                            else:
                                print(f" LIVE EXIT | {trade_id} | {msg}")
                        state.closed_trades.append(trade)
                        append_csv_row(TRADES_LOG_CSV, trade_to_log_row(trade))
                        print(
                            f" {execution_mode} TRADE OPEN+CLOSE | id={trade_id} | {option_symbol} | "
                            f"{side} | reason={trade['close_reason']} | net={trade['net_pnl_rupees']:.2f}"
                        )
                    else:
                        state.open_trades.append(trade)
                        append_csv_row(TRADES_LOG_CSV, trade_to_log_row(trade))
                        print(
                            f" {execution_mode} TRADE OPEN | id={trade_id} | {option_symbol} | {side} | "
                            f"entry={trade['opt_entry_price']:.2f} sl={trade['opt_sl_price']:.2f} t1={trade['opt_t1_price']:.2f}"
                        )

                except Exception as e:
                    log_error(
                        "process_signal",
                        e,
                        {
                            "signal_time": str(st),
                            "side": side,
                            "level_tag": level_tag,
                            "fut_entry": fut_entry,
                            "fut_sl": fut_sl,
                        },
                    )

            # Loop sleep
            time.sleep(POLL_EVERY_SECONDS)

        except KeyboardInterrupt:
            print("\n KeyboardInterrupt received. Exiting.")
            break
        except Exception as e:
            log_error("main_loop", e, {"trace": traceback.format_exc(limit=1)})
            time.sleep(POLL_EVERY_SECONDS)

    # Final summary
    print("\n" + "=" * 80)
    print("Runner stopped.")
    print(f"Closed trades this session: {len(state.closed_trades)}")
    if state.closed_trades:
        dfc = pd.DataFrame([trade_to_log_row(t) for t in state.closed_trades])
        if "net_pnl_rupees" in dfc.columns:
            pnl = pd.to_numeric(dfc["net_pnl_rupees"], errors="coerce").fillna(0)
            print(f"Session paper net PnL: {pnl.sum():,.2f}")
    print("=" * 80)


if __name__ == "__main__":
    main()
