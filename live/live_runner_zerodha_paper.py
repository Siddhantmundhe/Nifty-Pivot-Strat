def load_access_token() -> str:
    """
    Prefer file token (generated by generate_kite_token.py),
    fallback to env var. Strips whitespace/newline.
    """
    # 1) Prefer file first (safer in your workflow)
    if os.path.exists(ACCESS_TOKEN_FILE):
        with open(ACCESS_TOKEN_FILE, "r", encoding="utf-8") as f:
            token = f.read().strip()
        if token:
            print("DEBUG [auth] token source: file")
            return token

    # 2) Fallback to env var
    token = os.getenv("KITE_ACCESS_TOKEN", "").strip()
    if token:
        print("DEBUG [auth] token source: env")
        return token

    raise FileNotFoundError(
        f"Access token not found in {ACCESS_TOKEN_FILE} or KITE_ACCESS_TOKEN env var. "
        f"Run generate_kite_token.py first."
    )


