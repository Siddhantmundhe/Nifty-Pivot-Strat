def open_paper_trade_from_signal(
    signal,
    contract_row: pd.Series,
    option_candles: pd.DataFrame,
    execution_mode: str = "PAPER",
    forced_entry_price: Optional[float] = None,
    forced_entry_time: Optional[pd.Timestamp] = None,
) -> Dict[str, Any]:
    """
    Open paper trade using first option candle >= signal_time.
    """
    st = sig_time(signal)
    side = sig_side(signal)
    fut_entry = sig_entry(signal)
    fut_sl = sig_sl(signal)
    fut_tp = sig_tp(signal)
    level_tag = sig_level_tag(signal)
    level_price = sig_level_price(signal)

    if option_candles.empty or "date" not in option_candles.columns:
        raise RuntimeError("No option candle data for opening trade.")

    df = option_candles.copy()
    df["date"] = pd.to_datetime(df["date"], errors="coerce")
    df = df.dropna(subset=["date"]).sort_values("date").reset_index(drop=True)
    row_df = df[df["date"] >= st].copy()
    if row_df.empty:
        raise RuntimeError("No option candle at/after signal time for entry.")
    entry_candle = row_df.iloc[0]
    opt_entry_time = pd.to_datetime(entry_candle["date"], errors="coerce")
    opt_entry_price = float(entry_candle["close"])
    if forced_entry_time is not None and not pd.isna(forced_entry_time):
        opt_entry_time = pd.to_datetime(forced_entry_time, errors="coerce")
    if forced_entry_price is not None and float(forced_entry_price) > 0:
        opt_entry_price = float(forced_entry_price)

    # Proxy T1/SL from futures target points
    # Can be refined later (delta/vega model or exact exit timestamp mapping)
    assumed_delta = 0.50
    opt_t1_points = max(1.0, TARGET1_POINTS * assumed_delta)
    opt_t1_price = opt_entry_price + opt_t1_points
    opt_sl_price = max(0.05, opt_entry_price - (opt_t1_points * 0.5))
    opt_be_price = opt_entry_price

    trade = {
        "trade_id": None,  # set by engine
        "trade_date": str(st.date()),
        "execution_mode": str(execution_mode).upper(),
        "status": "OPEN",
        "stage": "FULL_OPEN",   # FULL_OPEN -> HALF_OUT -> CLOSED
        "entry_time_signal": st,
        "entry_time_option": opt_entry_time,
        "side": side,
        "level_tag": level_tag,
        "level_price": level_price,
        "fut_entry": fut_entry,
        "fut_sl": fut_sl,
        "fut_tp": fut_tp,
        "opt_symbol": str(contract_row["tradingsymbol"]),
        "opt_token": int(contract_row["instrument_token"]),
        "opt_expiry": contract_row.get("expiry"),
        "opt_strike": safe_float(contract_row.get("strike")),
        "opt_type": contract_row.get("instrument_type"),
        "opt_lot_size": int(contract_row.get("lot_size", 0)),
        "lots": LOTS,
        "opt_qty": int(max(1, int(contract_row.get("lot_size", 0) or 0) * LOTS)),
        "qty_fraction_open": 1.0,

        "opt_entry_price": opt_entry_price,
        "opt_sl_price": opt_sl_price,
        "opt_t1_price": opt_t1_price,
        "opt_be_price": opt_be_price,

        "exit1_time": None,
        "exit1_price": None,
        "exit2_time": None,
        "exit2_price": None,

        "realized_points_total": 0.0,  # per option point, weighted by fractions
        "gross_pnl_rupees": 0.0,
        "cost_total_rupees": 0.0,
        "net_pnl_rupees": 0.0,
        "close_reason": None,
        "last_update_time": _now_local(),
        "error": None,
        "live_entry_order_id": None,
        "live_exit_order_id": None,
        "live_entry_status": None,
        "live_exit_status": None,
        "live_product": LIVE_PRODUCT,
    }
    return trade


def _points_to_rupees(weighted_points_total: float, lot_size: int, lots: int) -> float:
    return weighted_points_total * lot_size * lots


def _compute_total_costs_rupees(lots: int) -> float:
    # total roundtrip cost for full position (2 lots etc.)
    slippage_total = SLIPPAGE_PER_SIDE_RUPEES * 2 * lots
    charges_total = CHARGES_PER_LOT_ROUNDTRIP * lots
    return slippage_total + charges_total


def _update_trade_from_option_candles(trade: Dict[str, Any], option_candles: pd.DataFrame, now: pd.Timestamp) -> Dict[str, Any]:
    """
    Updates an OPEN trade using all option candles >= entry time up to now.
    Conservative intrabar assumptions when both SL and T1 occur in same candle.
    """
    if trade["status"] != "OPEN":
        return trade

    if option_candles.empty or "date" not in option_candles.columns:
        trade["last_update_time"] = now
        return trade

    df = option_candles.copy()
    df["date"] = pd.to_datetime(df["date"], errors="coerce")
    df = df.dropna(subset=["date"]).sort_values("date").reset_index(drop=True)

    entry_time = pd.to_datetime(trade["entry_time_option"], errors="coerce")
    if pd.isna(entry_time):
        trade["error"] = "Invalid entry_time_option"
        trade["last_update_time"] = now
        return trade

    # Process candles after entry time (including entry candle)
    df = df[(df["date"] >= entry_time) & (df["date"] <= now)].copy().reset_index(drop=True)
    if df.empty:
        trade["last_update_time"] = now
        return trade

    entry_price: float = float(trade["opt_entry_price"])
    sl_price: float = float(trade["opt_sl_price"])
    t1_price: float = float(trade["opt_t1_price"])
    be_price: float = float(trade["opt_be_price"])

    stage: str = str(trade["stage"])
    execution_mode = str(trade.get("execution_mode", "PAPER")).upper()
    realized_points_total: float = float(trade["realized_points_total"])
    half: float = 0.5
    rem: float = 0.5

    # If already partially closed, ignore candles before exit1_time
    if stage == "HALF_OUT" and trade.get("exit1_time") is not None:
        exit1_time = pd.to_datetime(trade["exit1_time"], errors="coerce")
        df = df[df["date"] >= exit1_time].copy().reset_index(drop=True)

    for _, row in df.iterrows():
        t = pd.to_datetime(row["date"], errors="coerce")
        h: float = float(row["high"])
        l: float = float(row["low"])

        if stage == "FULL_OPEN":
            sl_hit = l <= sl_price
            t1_hit = h >= t1_price

            if sl_hit and t1_hit:
                # Conservative: assume SL first (worst-case) for full qty
                trade["exit1_time"] = t
                trade["exit1_price"] = sl_price
                trade["exit2_time"] = t
                trade["exit2_price"] = sl_price
                realized_points_total = (sl_price - entry_price) * 1.0 # type: ignore
                trade["status"] = "CLOSED"
                trade["stage"] = "CLOSED"
                trade["close_reason"] = "SL_HIT_AMBIGUOUS_BEFORE_T1"
                break

            if sl_hit:
                trade["exit1_time"] = t
                trade["exit1_price"] = sl_price
                trade["exit2_time"] = t
                trade["exit2_price"] = sl_price
                realized_points_total = (sl_price - entry_price) * 1.0 # type: ignore
                trade["status"] = "CLOSED"
                trade["stage"] = "CLOSED"
                trade["close_reason"] = "SL_HIT"
                break

            if t1_hit:
                if execution_mode == "LIVE":
                    # Live mode uses full-quantity exit on target trigger to keep execution deterministic.
                    trade["exit1_time"] = t
                    trade["exit1_price"] = t1_price
                    trade["exit2_time"] = t
                    trade["exit2_price"] = t1_price
                    realized_points_total = (t1_price - entry_price) * 1.0 # type: ignore
                    trade["status"] = "CLOSED"
                    trade["stage"] = "CLOSED"
                    trade["qty_fraction_open"] = 0.0
                    trade["close_reason"] = "T1_HIT_FULL_LIVE"
                    break
                trade["exit1_time"] = t
                trade["exit1_price"] = t1_price
                realized_points_total += (t1_price - entry_price) * half # type: ignore
                trade["qty_fraction_open"] = rem
                trade["stage"] = "HALF_OUT"
                trade["close_reason"] = "T1_HIT_PARTIAL"
                stage = "HALF_OUT"
                continue

        elif stage == "HALF_OUT":
            be_hit = l <= be_price
            if be_hit:
                trade["exit2_time"] = t
                trade["exit2_price"] = be_price
                realized_points_total += (be_price - entry_price) * rem # type: ignore
                trade["status"] = "CLOSED"
                trade["stage"] = "CLOSED"
                trade["qty_fraction_open"] = 0.0
                trade["close_reason"] = "T1_THEN_BE"
                break

    # Force EOD close if still open and time passed
    force_exit_ts = _today_time(FORCE_EXIT_TIME, now)
    if trade["status"] == "OPEN" and now >= force_exit_ts and not df.empty:
        last = df.iloc[-1]
        last_t = pd.to_datetime(last["date"], errors="coerce")
        last_c = float(last["close"])
        if stage == "FULL_OPEN":
            trade["exit1_time"] = last_t
            trade["exit1_price"] = last_c
            realized_points_total = (last_c - entry_price) * 1.0 # type: ignore
            trade["close_reason"] = "EOD_EXIT_FULL"
        elif stage == "HALF_OUT":
            trade["exit2_time"] = last_t
            trade["exit2_price"] = last_c
            realized_points_total += (last_c - entry_price) * rem # type: ignore
            trade["close_reason"] = "T1_THEN_EOD"
        trade["status"] = "CLOSED"
        trade["stage"] = "CLOSED"
        trade["qty_fraction_open"] = 0.0

    # Recompute PnL
    trade["realized_points_total"] = realized_points_total
    lot_size = int(trade.get("opt_lot_size", 0) or 0)
    gross = _points_to_rupees(realized_points_total, lot_size, int(trade["lots"])) if lot_size > 0 else 0.0
    costs = _compute_total_costs_rupees(int(trade["lots"]))
    trade["gross_pnl_rupees"] = gross
    trade["cost_total_rupees"] = costs
    trade["net_pnl_rupees"] = gross - costs
    trade["last_update_time"] = now

    return trade


