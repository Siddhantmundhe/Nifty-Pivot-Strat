def get_underlying_token(
    nse_df: pd.DataFrame,
    nfo_df: pd.DataFrame,
    symbol: str,
    fut_name: str,
    signal_mode: str = "FUT",
) -> Tuple[int, str]:
    mode = str(signal_mode).upper().strip()

    if mode == "SPOT":
        # For index spot, Zerodha instruments('NSE') usually has tradingsymbol = "NIFTY 50"
        x = nse_df[nse_df["tradingsymbol"].astype(str).str.upper() == symbol.upper()].copy()
        if x.empty:
            x = nse_df[nse_df["tradingsymbol"].astype(str).str.upper().str.contains(symbol.upper(), na=False)].copy()
        if x.empty:
            raise RuntimeError(f"Underlying symbol not found in NSE instruments: {symbol}")
        row = x.iloc[0]
        return int(row["instrument_token"]), str(row.get("tradingsymbol", symbol))

    # FUT mode: nearest non-expired index future
    fut = nfo_df.copy()
    for col in ["segment", "name", "expiry", "instrument_token", "tradingsymbol"]:
        if col not in fut.columns:
            raise RuntimeError(f"NFO instruments missing required column: {col}")

    fut = fut[fut["segment"].astype(str).str.upper() == "NFO-FUT"].copy()
    fut = fut[fut["name"].astype(str).str.upper() == str(fut_name).upper()].copy()
    fut["expiry"] = pd.to_datetime(fut["expiry"], errors="coerce")
    fut = fut.dropna(subset=["expiry"]).copy()

    today = _now_local().date()
    fut = fut[fut["expiry"].dt.date >= today].copy()
    if fut.empty:
        raise RuntimeError(f"No active futures found for {fut_name}")

    fut = fut.sort_values(["expiry", "tradingsymbol"]).reset_index(drop=True)
    row = fut.iloc[0]
    return int(row["instrument_token"]), str(row["tradingsymbol"])


def _debug_latest_candle_no_signal(prepared_df: pd.DataFrame, latest_ts: pd.Timestamp) -> None:
    try:
        if latest_ts not in prepared_df.index:
            print(f"[debug] no-row-for-latest-candle ts={latest_ts}")
            return

        row_any = prepared_df.loc[latest_ts]
        row = row_any.iloc[-1] if isinstance(row_any, pd.DataFrame) else row_any

        o = float(row["open"])
        h = float(row["high"])
        l = float(row["low"])
        c = float(row["close"])
        body = abs(c - o)
        rng = abs(h - l)
        hhmm = int(latest_ts.hour) * 100 + int(latest_ts.minute)
        cutoff = SIGNAL_ENTRY_CUTOFF_HHMM if SIGNAL_ENTRY_CUTOFF_HHMM is not None else 1400

        print(
            f"[debug] latest-candle ts={latest_ts} ohlc=({o:.2f},{h:.2f},{l:.2f},{c:.2f}) "
            f"body={body:.2f} range={rng:.2f} hhmm={hhmm} cutoff={cutoff}"
        )

        reasons: List[str] = []
        if hhmm > cutoff:
            reasons.append(f"entry-cutoff-fail({hhmm}>{cutoff})")
        if body < float(MIN_BODY_POINTS):
            reasons.append(f"min-body-fail({body:.2f}<{float(MIN_BODY_POINTS):.2f})")
        if MAX_CANDLE_RANGE_POINTS is not None and rng > float(MAX_CANDLE_RANGE_POINTS):
            reasons.append(f"range-fail({rng:.2f}>{float(MAX_CANDLE_RANGE_POINTS):.2f})")

        if reasons:
            print("[debug] hard-filters:", ", ".join(reasons))

        levels = []
        for nm in ["PP", "R1", "R2", "S1", "S2"]:
            lv = row.get(nm)
            if pd.notna(lv):
                levels.append((nm, float(lv)))

        checks: List[str] = []
        for nm, lv in levels:
            touched = (l - TOUCH_TOLERANCE) <= lv <= (h + TOUCH_TOLERANCE)
            long_ok = touched and (c > (lv + BREAK_CONFIRMATION)) and (c >= o)
            short_ok = touched and (c < (lv - BREAK_CONFIRMATION)) and (c <= o)
            enabled = (
                (nm == "PP" and (ALLOW_LONG_PP_SIGNAL or ALLOW_SHORT_PP_SIGNAL))
                or (nm in {"R1", "R2"})
                or (nm == "S1")
            )
            checks.append(
                f"{nm}:touch={touched} long={long_ok} short={short_ok} enabled={enabled} lv={lv:.2f}"
            )

        print("[debug] level-checks | " + " | ".join(checks))
    except Exception as e:
        print(f"[debug] failed-to-explain-no-signal: {e}")


