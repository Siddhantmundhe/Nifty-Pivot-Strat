def get_order_snapshot(kite: KiteConnect, order_id: str) -> Optional[dict]:
    orders = kite.orders()
    for o in reversed(orders):
        if str(o.get("order_id")) == str(order_id):
            return o
    return None


def wait_for_order_status(
    kite: KiteConnect,
    order_id: str,
    statuses: Set[str],
    timeout_sec: int,
    poll_sec: float = 1.0,
) -> Optional[dict]:
    start = time.time()
    target = {str(s).upper() for s in statuses}
    while time.time() - start <= timeout_sec:
        snap = get_order_snapshot(kite, order_id)
        if snap and str(snap.get("status", "")).upper() in target:
            return snap
        time.sleep(poll_sec)
    return get_order_snapshot(kite, order_id)


def _order_avg_fill_price(order_snap: Optional[dict], fallback: float) -> float:
    if not order_snap:
        return float(fallback)
    for k in ("average_price", "avg_price", "price"):
        v = safe_float(order_snap.get(k))
        if v is not None and v > 0:
            return float(v)
    return float(fallback)


