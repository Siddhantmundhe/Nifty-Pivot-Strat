def signal_to_log_row(signal) -> Dict[str, Any]:
    return {
        "logged_at": str(_now_local()),
        "signal_time": str(sig_time(signal)),
        "side": sig_side(signal),
        "level_tag": sig_level_tag(signal),
        "level_price": sig_level_price(signal),
        "fut_entry": sig_entry(signal),
        "fut_sl": sig_sl(signal),
        "fut_tp": sig_tp(signal),
    }


def trade_to_log_row(trade: Dict[str, Any]) -> Dict[str, Any]:
    # flatten timestamps
    row = dict(trade)
    for k in list(row.keys()):
        v = row[k]
        if isinstance(v, (pd.Timestamp, datetime)):
            row[k] = str(v)
    row["logged_at"] = str(_now_local())
    return row


def closed_net_pnl_rupees(closed_trades: List[Dict[str, Any]]) -> float:
    total = 0.0
    for tr in closed_trades:
        total += safe_float(tr.get("net_pnl_rupees"), 0.0) or 0.0
    return float(total)


def flatten_live_trade(kite: KiteConnect, trade: Dict[str, Any]) -> Tuple[bool, str]:
    symbol = str(trade.get("opt_symbol") or "")
    qty = int(trade.get("opt_qty") or 0)
    if not symbol or qty <= 0:
        return False, f"Invalid live flatten payload: symbol={symbol} qty={qty}"

    try:
        oid = place_market_order(
            kite=kite,
            exchange=OPTION_EXCHANGE,
            tradingsymbol=symbol,
            tx_type=kite.TRANSACTION_TYPE_SELL,
            qty=qty,
            product=LIVE_PRODUCT,
        )
        snap = wait_for_order_status(kite, oid, {"COMPLETE", "REJECTED", "CANCELLED"}, LIVE_ORDER_TIMEOUT_SEC)
        status = str((snap or {}).get("status", "UNKNOWN")).upper()
        trade["live_exit_order_id"] = str(oid)
        trade["live_exit_status"] = status
        if status != "COMPLETE":
            return False, f"Exit order not complete. order_id={oid} status={status}"
        return True, f"Exit order complete. order_id={oid}"
    except Exception as e:
        return False, f"Live flatten failed: {e}"


